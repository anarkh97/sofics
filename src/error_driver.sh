#!/bin/bash

# -----------------------------------------------------------------------------
# Run analysis in background
# -----------------------------------------------------------------------------

"$FEST_EXE" "$WORKING_DIR/$FEST_ERROR_INPUT" 2>&1 \
  > >(tee "$WORKING_DIR/log.out" > /dev/null) 2>&1 &

pid=$!

printf "\033[34mLaunching (error) Evaluation %s on nodes " "${DAK_EVAL_NUM}"
printf "%s with process id %s.\033[0m\n" "${host_list[*]}" "$pid"

# -----------------------------------------------------------------------------
# Wait for completion
# -----------------------------------------------------------------------------

wait "$pid"

# -----------------------------------------------------------------------------
# Exit assuming everything was successfull
# -----------------------------------------------------------------------------
exit 0
