*****   GTIMER ... return cpu time                                 *****
************************************************************************
************************************************************************
*
*   --------
*   Purpose:
*   --------
*
*     This function returns the current cpu time.
*
************************************************************************
*
      REAL FUNCTION  GTIMER  ()
*
************************************************************************
*
*=======================================================================
*       Segment for IBM RS/6000 ...
*=======================================================================
*
*       ----------------------
*       External Functions ...
*       ----------------------
*       INTEGER             MCLOCK
*       EXTERNAL            MCLOCK
*
************************************************************************
*
*       GTIMER = MCLOCK()/100.0
*
*=======================================================================
*       End of segment for IBM RS/6000.
*=======================================================================
*
*
*=======================================================================
*       Segment for most Berkeley UNIX ...
*=======================================================================
*
*       -------------------------
*       Local Array Variables ...
*       -------------------------
        REAL        VEC(2)
*
*       ----------------------
*       External Functions ...
*       ----------------------
        REAL                ETIME
        EXTERNAL            ETIME
*
************************************************************************
*
        GTIMER = ETIME(VEC)
*
*=======================================================================
*       End of segment for most Berkeley UNIX.
*=======================================================================
*
*
*=======================================================================
*       Segment for Cray ...
*=======================================================================
*
*       ----------------------
*       External Functions ...
*       ----------------------
*       REAL                SECOND
*       EXTERNAL            SECOND
*
************************************************************************
*
*       GTIMER = SECOND()
*
*=======================================================================
*       End of Segment for Cray.
*=======================================================================
*
        RETURN
*
*       --------------
*       End of GTIMER.
*       --------------
      END
